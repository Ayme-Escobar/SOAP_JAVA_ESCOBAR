    private void btnMmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMmActionPerformed
        // TODO add your handling code here:
        // Validar entrada
        if (!validarEntrada()) {
            return; 
        }
        //Datos
        double cm = Double.parseDouble(txtCm.getText());        
        //Proceso
        ConvUniController control = new ConvUniController();
        double convMm = control.cmAMm(cm);
        
        //Reporte
        String repo="";
        repo += cm + " cm en Milimetros = "+ df.format(convMm) + " mm"+"\n";
        JOptionPane.showMessageDialog(rootPane, repo);

        
    }//GEN-LAST:event_btnMmActionPerformed

