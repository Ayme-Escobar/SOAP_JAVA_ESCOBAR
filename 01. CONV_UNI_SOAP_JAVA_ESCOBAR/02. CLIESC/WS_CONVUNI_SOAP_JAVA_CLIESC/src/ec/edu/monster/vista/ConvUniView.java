    private boolean validarEntrada() {
        String inputText = txtCm.getText().replace(",", ".");
        
        if (!inputText.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, ingrese un número válido en centímetros.");
            txtCm.setText("");
            return false;
        }
        
        txtCm.setText(inputText);

        return true;
    }
